package com.teamsankya.calculater.calculater;

public class Divition {
	
	
	public static int divition (int a , int b) 
	{
		return a/b;
	}
	
	
	public static long divition (long a , long b) 
	{
		return a/b;
	}
	public static float divition (float a , float b) 
	{
		return a/b;
	}
	public static double divition (double a , double b) 
	{
		return a/b;
	}
	public static byte divition (byte a , byte b) 
	{
		return  (byte) (a/b);
	}
	}
