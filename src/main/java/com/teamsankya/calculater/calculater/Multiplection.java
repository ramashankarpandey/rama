package com.teamsankya.calculater.calculater;

public class Multiplection {
	
	public static int multiplecation (int a , int b) 
	{
		return a*b;
	}
	
	
	public static long multiplecation (long a , long b) 
	{
		return a*b;
	}
	public static float multiplecation (float a , float b) 
	{
		return a*b;
	}
	public static double multiplecation (double a , double b) 
	{
		return a*b;
	}
	public static byte multiplecation (byte a , byte b) 
	{
		return  (byte) (a*b);
	}
	}
