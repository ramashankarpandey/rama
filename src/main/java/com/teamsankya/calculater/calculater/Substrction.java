package com.teamsankya.calculater.calculater;

public class Substrction {
	
	public static int substraction (int a , int b) 
	{
		return a-b;
	}
	
	
	public static long substraction (long a , long b) 
	{
		return a-b;
	}
	public static float substraction (float a , float b) 
	{
		return a-b;
	}
	public static double substraction (double a , double b) 
	{
		return a-b;
	}
	public static byte substraction (byte a , byte b) 
	{
		return  (byte) (a-b);
	}
	
	}
